package edunova;

public class Inkrement {
	
	public static void main(String[] args) {
		
		int i=5;
		
		i=i+1; //6
		i+=1; //7
		
		i++; //8 inkrement
		System.out.println(i++); //8 //i++ koristiti prvo i pa ga onda uvećati
		System.out.println(i); //9
		
		System.out.println(++i); //10 ++i prvo uveća a onda koristi
		
		int t=2;
		//2
		t = t+1;
		//3
		t++;
		//4
		++t;
		//5
		t = ++t +1;
		//7
		System.out.println(t);//7 8 10 7 7
		
		
		
		
		
		
	}

}
